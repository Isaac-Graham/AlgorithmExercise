//假设按照升序排序的数组在预先未知的某个点上进行了旋转。 
//
// ( 例如，数组 [0,0,1,2,2,5,6] 可能变为 [2,5,6,0,0,1,2] )。 
//
// 编写一个函数来判断给定的目标值是否存在于数组中。若存在返回 true，否则返回 false。 
//
// 示例 1: 
//
// 输入: nums = [2,5,6,0,0,1,2], target = 0
//输出: true
// 
//
// 示例 2: 
//
// 输入: nums = [2,5,6,0,0,1,2], target = 3
//输出: false 
//
// 进阶: 
//
// 
// 这是 搜索旋转排序数组 的延伸题目，本题中的 nums 可能包含重复元素。 
// 这会影响到程序的时间复杂度吗？会有怎样的影响，为什么？ 
// 
// Related Topics 数组 二分查找


#include<bits/stdc++.h>

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    bool binary_search(vector<int> &nums, int begin, int end, int target) {
        if (begin == end) {
            return nums[begin] == target;
        } else if (end - begin == 1) {
            return nums[begin] == target || nums[end] == target;
        }

        int mid = (begin + end) / 2;
        if (nums[mid] == target) {
            return true;
        } else if (nums[mid] < target) {
            return binary_search(nums, mid + 1, end, target);
        } else {
            return binary_search(nums, begin, mid, target);
        }
    }

    bool search(vector<int> &nums, int target) {
        if (nums.size() == 0) {
            return false;
        } else if (nums.size() == 1) {
            return nums[0] == target;
        }
        int begin = 0, end = nums.size() - 1;
        int diff = 0;
        while (begin < end) {
            diff = (begin + end) / 2;
            if (nums[diff] > nums[begin]) {
                begin = diff;
            } else if (nums[diff] < nums[begin]) {
                end = diff;
            } else {
                begin++;
            }
        }
        return binary_search(nums, 0, diff, target) || binary_search(nums, diff + 1, nums.size() - 1, target);
    }
};
